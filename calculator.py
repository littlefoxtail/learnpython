#encoding:utf-8
"""
python中的计算
"""

# 除法的话返回的类型是带小数点的
print((60 - 5 * 6) / 4)

# 这么除放弃小数点
print(17 // 3)

# 取余数
print(17 % 3)

# 取指数
# 平方
print(5 ** 2)

print(2 ** 7)

# 多种数据进行运算，整形会转成浮点数
print(4 * 3.751 - 1)

# 虚数
print((3 + 5j) + 2)

# 绝对值
print(abs(-12 + 5))

# 转成整形
print(int(4.545 + 3.332))

# 转成浮点型
print(float(121 + 212))

# Pair (余数,)
print(divmod(2, 5))







